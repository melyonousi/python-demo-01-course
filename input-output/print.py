# normal print with double quotes
print("hello world!")

# normal print with single quotes
print('hello world!')

# print with comma separated
print("hello", "world!")
print("hello", "world!", "from", "Morocco")

# print with custom separator
print("hello", "world!", "from", "Morocco", sep='+')

# print with custom end line
print("hello", "world!", "from", "Morocco", sep='+', end=".\n")

# print with long text
print("هذا النص هو مثال لنص يمكن أن يستبدل\
 في نفس المساحة، لقد تم توليد هذا النص من\
 مولد النص العربى، حيث يمكنك أن تولد مثل هذا النص أو العديد من \
النصوص الأخرى إضافة إلى زيادة عدد الحروف التى يولدها التطبيق.")
